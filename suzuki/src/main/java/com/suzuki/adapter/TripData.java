package com.suzuki.adapter;

public class TripData {
    String date;
    String time;
    String startLoc;
    String endLoc;
    public TripData(String date, String time, String startLoc,String endLoc)
    {
        this.date = date;
        this.time = time;
        this.startLoc = startLoc;
        this.endLoc = endLoc;
    }
}